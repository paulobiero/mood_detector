package main;

public class nodes {
    int soundPitch;
    int soundLength;
    int numberOfPauses;

    public int getSoundPitch() {
        return soundPitch;
    }

    public void setSoundPitch(int soundPitch) {
        this.soundPitch = soundPitch;
    }

    public int getSoundLength() {
        return soundLength;
    }

    public void setSoundLength(int soundLength) {
        this.soundLength = soundLength;
    }

    public int getNumberOfPauses() {
        return numberOfPauses;
    }

    public void setNumberOfPauses(int numberOfPauses) {
        this.numberOfPauses = numberOfPauses;
    }
}
